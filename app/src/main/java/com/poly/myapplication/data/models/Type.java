package com.poly.myapplication.data.models;

public class Type {
    private int type;
    private String name;

    public Type() {
    }

    public Type(int type, String name) {
        this.type = type;
        this.name = name;
    }

    public int getType() {
        return type;
    }

    public void setType(int type) {
        this.type = type;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
